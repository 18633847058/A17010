public class Contact {

    private String name;

    private String ID;

    private String BDID;

    private int type;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getBDID() {
        return BDID;
    }

    public void setBDID(String BDID) {
        this.BDID = BDID;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }
}
